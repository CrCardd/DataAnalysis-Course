def iterate(matriz):
    l = -1
    c = -1
    indices_valores = []
    for i in range(len(matriz[0]), 0, -2):

        for _ in range(i):
            l+=1
            c+=1
            indices_valores.append((matriz[l][c], matriz[c][l]))
        c-=1
        for _ in range(i-1):
            indices_valores.append((matriz[l][c], matriz[c][l]))
            l-=1
            c-=1
        l+=1

    return indices_valores


